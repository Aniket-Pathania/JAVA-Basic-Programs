package com.company;
public class DO_While_Loop_Concept_code {
    public static void main(String[] args){
        System.out.println("Print numbers from 1 to 45 using Do while");
        int a = 1;
        do {
            System.out.println(a);
            a+=1;
        }while (a<=45);
        System.out.println("End");
    }
}
