package com.company;
public class Print_from_100_to_200_using_While_Loop {
    public static void main(String[] args){
        System.out.println("Print from 100 to 200 ");
        int Start = 100;
        while (Start<=200){

            System.out.println(Start);
            Start += 1;

        }
        System.out.println("The numbers are listed from 100 to 200");
    }
}
